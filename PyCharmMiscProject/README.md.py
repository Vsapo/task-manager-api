# # Task Manager API
#
# Простое backend-приложение для управления задачами, написанное на Python с использованием FastAPI.
#
# ## 🚀 Возможности
#
# - Получить список задач
# - Получить задачу по ID
# - Добавить задачу
# - Отметить задачу выполненной
# - Удалить задачу
# - Сохранение данных в JSON
#
# ## 🛠 Технологии
#
# - Python
# - FastAPI
# - Uvicorn
# - JSON
#
